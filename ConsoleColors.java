package java_depomini_proje;

public class ConsoleColors {

    public static final String RESET = "\u001B[0m";   // Renk sıfırlama
    public static final String RED = "\u001B[31m";    // Kırmızı (Hata Mesajları)
    public static final String GREEN = "\u001B[32m";  // Yeşil (Başarı Mesajları)
    public static final String YELLOW = "\u001B[33m"; // Sarı (Menü ve Uyarılar)
    public static final String BLUE = "\u001B[34m";   // Mavi (Başlıklar)
    public static final String CYAN = "\u001B[36m";   // Cyan (Başlıklar)
    public static final String PURPLE = "\u001B[35m";   // Mor (Başlıklar)



    public static final String WHITE = "\u001B[46m";   // Beyaz (Arka Plan)
}
